package Kiosk;

public class Menu {
    // 커피, 주스, 디저트 큰 카테고리
    String name; //메뉴 이름
    String explanation; // 메뉴 설명

    public Menu() {
    } //기본 생성자

    public Menu(String name, String explanation) {
        this.name = name;
        this.explanation = explanation;
    } //매개변수가 있는 생성자
}



